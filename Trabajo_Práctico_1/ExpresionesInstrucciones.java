package trabajoPractico1;

public class ExpresionesInstrucciones {

    public static void main(String[] args) {
        int x = 10;         // Línea 1
        x = x + 5;          // Línea 2
        System.out.println(x); // Línea 3
        
        /*
        Clasificación:
        Línea 1 → Instrucción de declaración y asignación.
        Línea 2 → Instrucción de asignación.
        Línea 3 → Instrucción de llamada a método, con expresión como argumento (x).

        Diferencia:
        Expresión: Fragmento de código que produce un valor (x + 5).
        Instrucción: Línea completa que realiza una acción (asignar, declarar una variable, etc).
        */
    }
    
}
